# Este programa irá calcular a área de um quadrado. Peça para a pessoa informar a medida numérica de um lado do quadrado. E depois informe-lhe o valor da área do quadrado.

lado = int(input('Informe o valor de um lado do quadrado: '))

area_quadrado = lado * lado

print(f'A área do quadrado é: {area_quadrado}')
