# Colete a idade de 3 pessoas emostre a média de suas idades

pessoa_um = int(input('Pessoa 01 - Qual a sua idade? '))
pessoa_dois = int(input('Pessoa 02 - Qual a sua idade? '))
pessoa_tres = int(input('Pessoa 03 - Qual a sua idade?'))

media = (pessoa_um + pessoa_dois + pessoa_tres)/3

print(f'A média de idade é {media}')
